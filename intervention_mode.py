
def start_intervention_mode(goal):
    print("=== 介入プラン実践モード：未来支援設計ゲーム ===\n")

    print(f"🎯 合意された生活行為目標：{goal}\n")

    print("山口美咲さん（52歳・女性）についての初期情報：")
    print("- 若年性認知症により、最近自室が散らかっており物を探すのに時間がかかっている")
    print("- 仕事には週5日パートで通勤中。娘（教員・結婚予定）と同居。土日は休み")
    print("- スマホは操作可能で、もっと活用したいという意欲がある\n")

    score = 0

    print("📌 第一介入フェーズ（1ヶ月目）")
    print("問題：部屋が散らかっていて、物を探すのに時間がかかる → 出勤遅れのリスク")
    print("支援プランを選んでください：")
    print("1: 土曜に訪問リハで一緒に整理・スマホやカレンダーの支援を行う")
    print("2: 娘さんに整理を任せ、OTは電話で支援する")
    print("3: 何も介入しない（見守り）")
    ans1 = input("番号を入力：")
    if ans1 == "1":
        print("→ 訪問リハでの支援により、自分で物を探せる範囲が増えてきた。")
        score += 10
    elif ans1 == "2":
        print("→ 娘の手伝いで整理されたが、山口さん自身のスキル向上は限定的。")
        score += 5
    else:
        print("→ 状況変わらず、探し物に時間を費やしストレス増大。")

    print("\n📌 第二フェーズ（2ヶ月目）")
    print("支援プランを選んでください：")
    print("1: 紙カレンダー＋日めくりを設置し、訪問時に活用練習")
    print("2: スマホのみ活用を勧める")
    print("3: 娘さんに予定の確認を任せる")
    ans2 = input("番号を入力：")
    if ans2 == "1":
        print("→ 紙＋デジタル併用で予定忘れが減少。山口さんも満足気。")
        score += 10
    elif ans2 == "2":
        print("→ 操作習熟は進んだが、活用の場面は限られた。")
        score += 6
    else:
        print("→ 娘任せで依存傾向が高まり、本人の自信はやや低下。")

    print("\n📌 第三フェーズ（3ヶ月目）")
    print("支援プランを選んでください：")
    print("1: 就労支援も含め、娘や会社と役割を話し合う")
    print("2: 職場に出勤記録を取ってもらう")
    print("3: 予定リストを朝にLINE送信するよう支援者に依頼")
    ans3 = input("番号を入力：")
    if ans3 == "1":
        print("→ 周囲の理解も深まり、山口さんは仕事への安心感が増した。")
        score += 10
    elif ans3 == "2":
        print("→ 出勤記録の可視化で本人にも継続意識が生まれた。")
        score += 7
    else:
        print("→ LINE連携は便利だが、タイミングによって見落としも。")

    print("\n=== 最終結果 ===")
    print(f"総合スコア：{score}/30点")
    if score >= 25:
        print("🌟 山口さんは生活の自立を保ち、週5日就労を維持できています！")
    elif score >= 18:
        print("◯ 目標の大半は達成されていますが、一部課題が残っています。")
    else:
        print("△ 支援の組み立てに再考が必要です。別のプランも試してみましょう。")

    input("\nEnterキーで終了します。")
